export default function DashboardLayout({ children }: { children: React.ReactNode }) {
  const { merchant, loading, logout } = useAuth()
  const router = useRouter()
  const pathname = usePathname()

  useEffect(() => {
    if (!loading && !merchant) router.replace('/auth/login')
  }, [merchant, loading, router])

  if (loading || !merchant) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="w-8 h-8 rounded-full border-2 border-brand border-t-transparent animate-spin" />
      </div>
    )
  }

  return (
    <div className="flex min-h-screen">
      {/* Sidebar */}
      <aside className="w-60 shrink-0 flex flex-col bg-surface border-r border-surface-border">
        {/* Logo */}
        <div className="h-16 flex items-center px-5 border-b border-surface-border">
          <div className="flex items-center gap-2.5">
            <div className="w-8 h-8 rounded-lg bg-brand flex items-center justify-center shadow-md shadow-brand/30">
              <span className="text-white font-bold text-sm">N</span>
            </div>
            <span className="text-white font-semibold text-base tracking-tight">Netten</span>
          </div>
        </div>

        {/* Nav */}
        <nav className="flex-1 py-4 px-3 space-y-0.5 overflow-y-auto">
          {NAV.map(item => {
            const isActive = pathname === item.href || (item.href !== '/dashboard' && pathname.startsWith(item.href))
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2 rounded-xl text-sm font-medium transition-all duration-100 ${
                  isActive
                    ? 'bg-brand/20 text-brand-light'
                    : 'text-gray-400 hover:text-white hover:bg-surface-hover'
                }`}
              >
                <span className={isActive ? 'text-brand' : ''}>{item.icon}</span>
                {item.label}
              </Link>
            )
          })}
        </nav>

        {/* User */}
        <div className="p-3 border-t border-surface-border">
          <div className="flex items-center gap-3 px-3 py-2">
            <div className="w-8 h-8 rounded-full bg-brand/30 flex items-center justify-center shrink-0">
              <span className="text-brand-light text-xs font-bold uppercase">
                {merchant.email[0]}
              </span>
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-white text-xs font-medium truncate">
                {merchant.businessName || merchant.email.split('@')[0]}
              </p>
              <p className="text-gray-500 text-xs truncate">{merchant.plan}</p>
            </div>
            <button
              onClick={logout}
              className="text-gray-500 hover:text-white transition-colors shrink-0"
              title="Sign out"
            >
              <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
              </svg>
            </button>
          </div>
        </div>
      </aside>

      {/* Main content */}
      <main className="flex-1 overflow-y-auto">
        {children}
      </main>
    </div>
  )
}
